  roleColor = computed(() => {
    switch (this.role()) {
      case 'Admin': return 'bg-indigo-600';
      case 'Agent': return 'bg-emerald-600';
      case 'Customer': return 'bg-blue-600';
      case 'ClaimsOfficer': return 'bg-amber-600';
      default: return 'bg-slate-600';
    }
  });

  roleInitial = computed(() => this.role()?.charAt(0) || 'U');

  constructor(
    private authService: AuthApiService,
    private router: Router
  ) {}

  ngOnInit(): void {
    this.role.set(localStorage.getItem('role'));
    this.userEmail.set(localStorage.getItem('email'));
    this.currentUrl.set(this.router.url);

    this.routerSub = this.router.events
      .pipe(filter(e => e instanceof NavigationEnd))
      .subscribe((e: any) => {
        this.currentUrl.set(e.urlAfterRedirects);
      });
  }

  ngOnDestroy(): void {
    this.routerSub?.unsubscribe();
  }

  isActive(route: string): boolean {
    const url = this.currentUrl();
    return url === route || url.startsWith(route + '/');
  }

  logout(): void {
    this.authService.logout();
    this.router.navigate(['/login']);
  }
